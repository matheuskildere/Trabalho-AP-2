public class People {
    private String name;
    private String email;
    private long cpf;


    public void setName(String name) {
        this.name = name;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public void setCpf(long cpf) {
        this.cpf = cpf;
    }
    public String getName() {
        return this.name;
    }

    public long getCpf() {
        return this.cpf;
    }
    public String getEmail() {
        return this.email;
    }
    

}